#include <unistd.h>

void	ft_print_numbers(void)
{
	int		loop;
	char	c;

	c = '0';
	loop = 0;
	while (loop <= 9)
	{
		write(1, &c, 1);
		c = c + 1;
		loop++;
	}
}
